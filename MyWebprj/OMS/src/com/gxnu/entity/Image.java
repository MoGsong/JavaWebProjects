package com.gxnu.entity;

import java.io.Serializable;

/**
 * 图片管理
 * @author MoHX
 *
 */
public class Image implements Serializable{
	private int id;
	private String name;//名称
	private String url;//链接地址
	private String yuliu;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getYuliu() {
		return yuliu;
	}
	public void setYuliu(String yuliu) {
		this.yuliu = yuliu;
	}
	public Image(int id, String name, String url, String yuliu) {
		super();
		this.id = id;
		this.name = name;
		this.url = url;
		this.yuliu = yuliu;
	}
	public Image(String name, String url, String yuliu) {
		super();
		this.name = name;
		this.url = url;
		this.yuliu = yuliu;
	}
	public Image() {
		super();
	}
	@Override
	public String toString() {
		return "Image [id=" + id + ", name=" + name + ", url=" + url + ", yuliu=" + yuliu + "]";
	}
	
}
