package com.gxnu.entity;

import java.io.Serializable;

/**
 * 对图片的CURD	
 * @author hjj
 *
 */
public class Picture implements Serializable {
	
	private int id;
	private String name;
	private String URL;
	private String content;
	public Picture(int id, String name, String uRL, String content) {
		super();
		this.id = id;
		this.name = name;
		URL = uRL;
		this.content = content;
	}
	public Picture() {
		super();
		
	}
	public Picture(String name, String uRL, String content) {
		super();
		this.name = name;
		URL = uRL;
		this.content = content;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getURL() {
		return URL;
	}
	public void setURL(String uRL) {
		URL = uRL;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	@Override
	public String toString() {
		return "Picture [id=" + id + ", name=" + name + ", URL=" + URL + ", content=" + content + "]";
	}
	
	
	
	

}
